# Randomly choose a word from the word_list and assign it to a variable called chosen_word.
chosen_word = random.choice(word_list)
already_guessed_letters = []
counter = 0
previous_counter = counter
lives = 6
display = []
for _ in chosen_word:
    display.append('_')

print(logo)
print(display)
while counter != len(chosen_word) and lives != 0:
    while True:
        guess = input("Guess a letter: ").lower()
        if len(guess) < 2 and guess.isalpha():
            break
        elif not guess.isalpha():
            print("Please input a alphabet.")
        else:
            print("Please enter only one character.")

    if guess in already_guessed_letters:
        print(f"You have already guessed letter '{guess}'.")
        continue
    else:
        already_guessed_letters.append(guess)
    # Logic to increment the counter and check if we have to put those letter in the display
    for index, letter in enumerate(chosen_word):
        if letter == guess:
            counter += 1
            display[index] = letter
    # If there is no change in the counter number than it means we have guessed the letter correctly.
    if previous_counter == counter:
        lives -= 1
        print(stages[lives])
    else:
        previous_counter = counter
        print(stages[lives])
    print(display)

if lives > 0:
    print("You win!!!!")
else:
    print("You are dead!! Hangman over.")
    print(f"The word was {chosen_word}")